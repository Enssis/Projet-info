package fr.insa.a6.utilities;

import java.util.*;

/**
 * 
 */
public class Point {

    public double posX;
    public double posY;

    /**
     * Default constructor
     */
    public Point() {
        posX = 0;
        posY = 0;
    }

    public Point(double x, double y) {
        posY = y;
        posX = x;
    }



}